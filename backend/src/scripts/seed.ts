async function seed() {
  console.log('üå± Starting database seed...');

  try {
    await dataSource.initialize();
    console.log('üì¶ Database connected');

    const storeRepo = dataSource.getRepository(Store);
    const categoryRepo = dataSource.getRepository(Category);
    const productRepo = dataSource.getRepository(Product);

    const existingStores = await storeRepo.count();

    if (existingStores > 0) {
      console.log('‚ö†Ô∏è  Database already has data. Skipping seed.');
      console.log('   Run db:reset first if you want to reseed.');
      await dataSource.destroy();

      return;
    }

    console.log('üìù Creating categories...');
    const savedCategories = await categoryRepo.save(categories);
    console.log(`   ‚úÖ Created ${savedCategories.length} categories`);

    console.log('üè™ Creating stores...');
    const savedStores = await storeRepo.save(stores);
    console.log(`   ‚úÖ Created ${savedStores.length} stores`);

    console.log('üì¶ Creating products...');

    for (const productData of products) {
      const product = productRepo.create({
        name: productData.name,
        description: productData.description,
        quantity: productData.quantity,
        price: productData.price,
        imageUrl: productData.imageUrl,
        storeId: savedStores[productData.storeIndex].id,
        categories: productData.categoryIndices.map((i) => savedCategories[i]),
      });
      await productRepo.save(product);
    }

    console.log(`   ‚úÖ Created ${products.length} products`);

    console.log('');
    console.log('üéâ Seed completed successfully!');
    console.log('');
    console.log('Summary:');
    console.log(`   - ${savedStores.length} stores`);
    console.log(`   - ${savedCategories.length} categories`);
    console.log(`   - ${products.length} products`);

    await dataSource.destroy();
  } catch (error) {
    console.error('‚ùå Seed failed:', error);
    process.exit(1);
  }
}

seed();

